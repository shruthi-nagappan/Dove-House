def load_processed_data():
    """Load CSV files generated by filter_substance_abuse.py"""
    print("\n" + "="*80)
    print("LOADING PROCESSED SUBSTANCE ABUSE DATA")
    print("="*80)
    
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    substance_diag = pd.read_csv(OUTPUT_DIR / 'substance_abuse_by_diagnosis.csv')
    category_summary = pd.read_csv(OUTPUT_DIR / 'substance_abuse_category_summary.csv')
    hospital_summary = pd.read_csv(OUTPUT_DIR / 'substance_abuse_hospital_summary.csv')
    hospital_category = pd.read_csv(OUTPUT_DIR / 'substance_abuse_by_hospital_category.csv')
    hospital_payer = pd.read_csv(OUTPUT_DIR / 'substance_abuse_by_hospital_payer.csv')
    payer_summary = pd.read_csv(OUTPUT_DIR / 'substance_abuse_payer_summary.csv')
    msdrg_summary = pd.read_csv(OUTPUT_DIR / 'substance_abuse_msdrg_summary.csv')
    
    print(f"✓ Loaded all summary CSV files")
    return substance_diag, category_summary, hospital_summary, hospital_category, hospital_payer, payer_summary, msdrg_summary

def plot_substance_categories(category_summary):
    """1. Substance category breakdown with admission counts"""
    fig, ax = plt.subplots(figsize=(14, 8))
    
    data = category_summary.sort_values('PATS', ascending=True)
    colors = plt.cm.Spectral(np.linspace(0, 1, len(data)))
    bars = ax.barh(data['SUBSTANCE_CATEGORY'], data['PATS'], color=colors)
    
    ax.set_xlabel('Number of Admissions', fontsize=12, fontweight='bold')
    ax.set_title('Substance Abuse & Overdose Admissions by Category\nIndiana 2024', 
                 fontsize=14, fontweight='bold', pad=20)
    ax.xaxis.set_major_formatter(plt.FuncFormatter(lambda x, p: f'{int(x/1000)}K'))
    
    for i, (bar, val) in enumerate(zip(bars, data['PATS'])):
        ax.text(val + 200, bar.get_y() + bar.get_height()/2, 
               f'{int(val):,}', va='center', fontweight='bold', fontsize=10)
    
    plt.tight_layout()
    VIZ_DIR.mkdir(parents=True, exist_ok=True)
    plt.savefig(VIZ_DIR / '01_substance_categories.png', dpi=300, bbox_inches='tight')
    print("✓ Saved: 01_substance_categories.png")
    plt.close()

def plot_category_costs(category_summary):
    """2. Average charge and LOS by substance category"""
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(16, 7))
    
    data = category_summary.sort_values('AVG_CHARGE', ascending=True)
    colors = plt.cm.RdYlGn_r(np.linspace(0.3, 0.7, len(data)))
    
    # Average charge
    bars1 = ax1.barh(data['SUBSTANCE_CATEGORY'], data['AVG_CHARGE'], color=colors)
    ax1.set_xlabel('Average Charge per Admission (USD)', fontsize=11, fontweight='bold')
    ax1.set_title('Average Cost by Substance Category', fontsize=12, fontweight='bold', pad=15)
    ax1.xaxis.set_major_formatter(plt.FuncFormatter(lambda x, p: f'${x/1000:.0f}K'))
    
    for bar, val in zip(bars1, data['AVG_CHARGE']):
        ax1.text(val + 1000, bar.get_y() + bar.get_height()/2, 
                f'${val:,.0f}', va='center', fontweight='bold', fontsize=9)
    
    # Average LOS
    data_los = category_summary.sort_values('AVG_LOS', ascending=True)
    colors_los = plt.cm.Blues(np.linspace(0.4, 0.9, len(data_los)))
    bars2 = ax2.barh(data_los['SUBSTANCE_CATEGORY'], data_los['AVG_LOS'], color=colors_los)
    ax2.set_xlabel('Average Length of Stay (days)', fontsize=11, fontweight='bold')
    ax2.set_title('Average Hospital Stay by Substance Category', fontsize=12, fontweight='bold', pad=15)
    
    for bar, val in zip(bars2, data_los['AVG_LOS']):
        ax2.text(val + 0.1, bar.get_y() + bar.get_height()/2, 
                f'{val:.2f}', va='center', fontweight='bold', fontsize=9)
    
    plt.tight_layout()
    plt.savefig(VIZ_DIR / '02_category_costs_los.png', dpi=300, bbox_inches='tight')
    print("✓ Saved: 02_category_costs_los.png")
    plt.close()

